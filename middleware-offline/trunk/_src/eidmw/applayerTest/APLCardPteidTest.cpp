TEST(ChallengeResponse)
{
	const char * const *list=AppLayer.readerList();
	APL_ReaderContext &reader2=AppLayer.getReader(list[2]);	
	APL_EIDCard *card2=reader2.getEIDCard();				

	CByteArray challenge1=card2->getChallenge();
	CByteArray response1=card2->getChallengeResponse();

	CHECK_EQUAL(true,card2->verifyChallengeResponse(challenge1,response1));

	CByteArray challenge2=card2->getChallenge();
	CByteArray response2=card2->getChallengeResponse();

	CHECK_EQUAL(true,challenge1.Equals(challenge2));
	CHECK_EQUAL(true,response1.Equals(response2));

	CByteArray challenge3=card2->getChallenge(true);
	CByteArray response3=card2->getChallengeResponse();

	CHECK_EQUAL(false,challenge1.Equals(challenge3));
	CHECK_EQUAL(false,response1.Equals(response3));

	CHECK_EQUAL(true,card2->verifyChallengeResponse(challenge3,response3));

	response3.SetByte(response3.GetByte(3)+1,3);

	CHECK_EQUAL(false,card2->verifyChallengeResponse(challenge3,response3));
}
